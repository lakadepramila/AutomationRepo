package dataTypesandVariables;

public class ArithmaticOperations {
	public static void main(String[] args)
	{
int a=10;
int b=20;

//Addition
System.out.println(a+b);

//Substraction
System.out.println(b-a);

//Division
System.out.println(a/b);
		
//multiplication
System.out.println(a*b);
		
//remainder
System.out.println(a%b);

	}
}
